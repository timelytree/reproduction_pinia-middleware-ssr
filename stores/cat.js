// ///////////////////////////////////////////////////////////////////// Imports
// -----------------------------------------------------------------------------
import { defineStore } from 'pinia'
import { ref } from '#imports'

// /////////////////////////////////////////////////////////////////////// State
// -----------------------------------------------------------------------------
const cat = ref('shabby')

// ///////////////////////////////////////////////////////////////////// Actions
// -----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////// setCat
const setCat = (payload) => {
  cat.value = payload
}

// ////////////////////////////////////////////////////////////////////// Export
// -----------------------------------------------------------------------------
export const useCatStore = defineStore('cat', () => ({
  // ----- state
  cat,
  // ----- actions
  setCat
}))
